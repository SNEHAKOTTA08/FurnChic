public class If
{
public static void main(String[] args)
{
int a=-1;

if(a==10)
{
System.out.println("a is equal to 10");
}

else if(a>=10)
{
System.out.println("a is greater than 10");
}

else if(a<=10)
{
System.out.println("a is smaller than 10");
}

else
{
System.out.println(" am out of if block");
}

}
}